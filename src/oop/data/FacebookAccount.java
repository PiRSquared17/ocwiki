package oop.data;

public class FacebookAccount {

	private long uid;
	private User user;

	public FacebookAccount() {
	}

	public long getUid() {
		return uid;
	}

	public void setUid(long uid) {
		this.uid = uid;
	}

	public User getUser() {
		return user;
	}

	public void setUser(User user) {
		this.user = user;
	}

}
